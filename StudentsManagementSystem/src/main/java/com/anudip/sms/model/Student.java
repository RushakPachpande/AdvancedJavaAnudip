package com.anudip.sms.model;

public class Student {

    private long id;
    private String name;
    private int rollNo;

    public Student() {
	super();
    }

    public Student(long id, String name, int rollNo) {
	super();
	this.id = id;
	this.name = name;
	this.rollNo = rollNo;
    }

    public long getId() {
	return id;
    }

    public void setId(long id) {
	this.id = id;
    }

    public String getName() {
	return name;
    }

    public void setName(String name) {
	this.name = name;
    }

    public int getRollNo() {
	return rollNo;
    }

    public void setRollNo(int rollNo) {
	this.rollNo = rollNo;
    }

}
